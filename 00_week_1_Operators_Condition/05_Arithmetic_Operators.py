print('-----------------[1 Arithmetic Operators]------------------')
print(10 + 5)    # Addition
print(10 - 5)    # Subtraction
print(10 * 5)    # Multiplication
print(10 / 5)    # Division 
print(10 % 3)    # Modulus (Remainder)
print(10 // 3)   # Floor Division
print(2 ** 3)    # Exponentiation
print('-----------------[2 Operator Precedence]------------------') #PEMDAS
print(10 + 5 * 2)        # Multiplication has higher precedence than addition
print((10 + 5) * 2)      # Parentheses change the order of operations
print('-----------------[3 Type Conversion with Arithmetic Operators]------------------')
print(5 + 2.0)    # int is converted to float, result is float

